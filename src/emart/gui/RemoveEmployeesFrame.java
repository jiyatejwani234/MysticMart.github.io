    private void loadAllEmpId() {
        try{
           jcEmpids.removeAllItems();
           List<String> empids=EmployeeDAO.getAllEmployeeId();
         for(String r:empids){
             jcEmpids.addItem(r);
         }
           
       }catch(SQLException e){
           JOptionPane.showMessageDialog(null,"could not load empids","error",JOptionPane.ERROR_MESSAGE);
           e.printStackTrace();
       }
    } 

   
