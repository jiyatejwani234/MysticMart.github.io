    private void jcEmpidsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcEmpidsActionPerformed
        String empid=(String)jcEmpids.getSelectedItem();
        try{
            EmployeePojo emp=EmployeeDAO.getEmployeeById(empid);
            txtEmpname.setText(emp.getEmpname());
            jcJob.setSelectedItem(emp.getJob());
            txtSalary.setText(String.valueOf(emp.getSalary()));
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,"record could not be loaded","error",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }

    }//GEN-LAST:event_jcEmpidsActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        
        try{
            boolean res=EmployeeDAO.deleteEmployee(jcEmpids.getSelectedItem().toString());
      
            if(res){
                JOptionPane.showMessageDialog(null,"record deleted successfully","success",JOptionPane.INFORMATION_MESSAGE);
                 loadAllEmpId();
                 
            }
            else{
                JOptionPane.showMessageDialog(null,"record could not be deleted","error",JOptionPane.ERROR_MESSAGE);

            }

        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null,"DB Error","error",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

