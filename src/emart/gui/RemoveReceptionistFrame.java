    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        try{
            String userid=jcReceptionist.getSelectedItem().toString();
            boolean r=ReceptionistDAO.deleteReceptionist(userid);
            if(r){
      JOptionPane.showMessageDialog(null,"receptionist deleted sucessfully!","Information",JOptionPane.INFORMATION_MESSAGE);
       jcReceptionist.removeItemAt(jcReceptionist.getSelectedIndex());
      if(jcReceptionist.getItemCount()==0)
       btnRemove.setEnabled(false);
       return;

         }
   JOptionPane.showMessageDialog(null," receptionist not deleted!","Information",JOptionPane.INFORMATION_MESSAGE);
 
            
        }
            catch(SQLException e){
             JOptionPane.showMessageDialog(null,"DBError","Error!",JOptionPane.ERROR_MESSAGE);
               }
        
    }//GEN-LAST:event_btnRemoveActionPerformed

