    private void loadReceptionistId() {
        try{
            List<String> al=ReceptionistDAO.getAllReceptionistUserId();
            if(al.isEmpty()){
                JOptionPane.showMessageDialog(null,"no receptionist present!","Information",JOptionPane.INFORMATION_MESSAGE);
            return;
            }
            btnRemove.setEnabled(true);
            for(String uid:al){
                jcReceptionist.addItem(uid);
                
            }}
        
            catch(SQLException e){
             JOptionPane.showMessageDialog(null,"DBError","Error!",JOptionPane.ERROR_MESSAGE);
               }
        }
    
